    public void paint(Graphics g) {
        for (int i = 0; i < deliverygraph.size; i++) {
            int startingXPoint = deliverygraph.getXCoordinate(i)*3 ;
            int startingYPoint = deliverygraph.getYCoordinate(i)*4;
            if (deliverygraph.getName(i).equalsIgnoreCase("depot")) {
                g.setColor(Color.RED);
                g.fillOval(startingXPoint, startingYPoint - 10, 20, 20);
            } else {
                g.setColor(Color.BLUE);
                g.fillRect(startingXPoint, startingYPoint, 20, 20);
            }
            for (int j = 1; j < deliverygraph.size; j++) {
                g.setColor(Color.GREEN);
                int endingXPoint = deliverygraph.getXCoordinate(j)*3;
                int endingYPoint = deliverygraph.getYCoordinate(j)*4;
                g.drawLine(startingXPoint, startingYPoint, endingXPoint, endingYPoint);
            }
        }
    }

